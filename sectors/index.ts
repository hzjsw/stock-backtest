/**
 * 根据板块代码获取股票列表
 */
export function getSymbolsBySector(sectorCode: string): string[] | undefined {
  const sector = SECTORS.find(s => s.code === sectorCode);
  return sector?.symbols;
}

/**
 * 获取所有板块配置
 */
export function getAllSectors(): SectorConfig[] {
  return SECTORS;
}

/**
 * 获取板块名称
 */
export function getSectorName(sectorCode: string): string | undefined {
  const sector = SECTORS.find(s => s.code === sectorCode);
  return sector?.name;
}

/**
 * 搜索板块
 */
export function searchSectors(keyword: string): SectorConfig[] {
  return SECTORS.filter(s =>
    s.name.includes(keyword) ||
    s.code.toLowerCase().includes(keyword.toLowerCase())
  );
}
